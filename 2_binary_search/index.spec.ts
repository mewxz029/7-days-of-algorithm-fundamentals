import { describe, it, expect } from 'bun:test'
import { findMin } from '.'

describe('findMin', () => {
  it('should return 1 from [1,2,3,4,5]', () => {
    expect(findMin([1, 2, 3, 4, 5])).toBe(1)
  })

  it('should return 2 from [4,5,6,2,3]', () => {
    expect(findMin([4, 5, 6, 2, 3])).toBe(2)
  })

  it('should return 0 from [4,5,6,7,0,1,2]', () => {
    expect(findMin([4, 5, 6, 7, 0, 1, 2])).toBe(0)
  })

  it('should return 11 from [11,11,11,11]', () => {
    expect(findMin([11, 11, 11, 11])).toBe(11)
  })

  it('should return 163 from [9769,9801,9988,163,277,398,514,981,1005,1358,1486,2047,2253,2336,2576,2593,2914,3146,3321,3769,4210,4303,4315,4321,4428,4805,5273,5436,5738,5971,6017,6153,6404,6419,6440,6484,6763,6786,6879,7061,7127,7247,8578,9176,9700]', () => {
    expect(findMin([9769, 9801, 9988, 163, 277, 398, 514, 981, 1005, 1358, 1486, 2047, 2253, 2336, 2576, 2593, 2914, 3146, 3321, 3769, 4210, 4303, 4315, 4321, 4428, 4805, 5273, 5436, 5738, 5971, 6017, 6153, 6404, 6419, 6440, 6484, 6763, 6786, 6879, 7061, 7127, 7247, 8578, 9176, 9700])).toBe(163)
  })
})
